package com.nextbest.skalkowski.whatwedo.data_model;


public class EditGroup {
    private int id;
    private String name;

    public EditGroup(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
